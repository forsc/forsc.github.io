// Blog card animations and interactions
const blogCards = document.querySelectorAll('.blog-card');

blogCards.forEach((card, index) => {
    // Staggered animation on scroll
    card.style.opacity = '0';
    card.style.transform = 'translateY(50px)';
    card.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
    
    const blogObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }, index * 100);
            }
        });
    }, { threshold: 0.1 });
    
    blogObserver.observe(card);
    
    // Read more button animation
    const readMoreBtn = card.querySelector('.blog-read-more');
    if (readMoreBtn) {
        readMoreBtn.addEventListener('mouseenter', () => {
            readMoreBtn.style.transform = 'translateX(5px)';
        });
        
        readMoreBtn.addEventListener('mouseleave', () => {
            readMoreBtn.style.transform = 'translateX(0)';
        });
    }
});

